        <div id="Mij">
            <h2>Over Mij</h2>
            <strong>Taxi Op Weg</strong> is door mij opgericht omdat die ene zonnige dag achter het stuur van een taxi, eind jaren negentig, in <a href="https://www.taxi13000.nl/" title="Homepage van Taxi13000 te Haarlem" target="_blank">Haarlem</a>, in mijn hoofd bleef hangen. 
            <br>Ik studeerde scheepsbouwkunde, maar ik vond het taxiwerk zo leuk, zeker die dag, dat ik het wel heel mijn leven zou kunnen doen in plaats van tijdelijk. 
            <br>Maar goed, dat deed ik natuurlijk niet. Ik studeerde af en ging werken. Werken op een kantoor. Maar ik miste wat, schoolde me om, 
            <br>werd leraar en verruilde het kantoor voor het klaslokaal. Een hele verbetering. Maar... ik miste wat... en stapte in 2018 voor <a href="https://www.taxisteketee.nl/" title="Homepage van Taxi Steketee" target="_blank">Taxi Steketee</a> weer in een taxi als taxichauffeur. 
            <br>Toch miste ik nog steeds wat: vrijheid. 
            Na veel wikken en wegen, terugdenkend aan die ene dag in Haarlem en de afgelopen 25 jaar overziend, heb ik uiteindelijk de knoop doorgehakt en een eigen taxi gekocht. Het resultaat is <strong>Taxi Op Weg</strong>. Mijn beste beslissing ooit.
            <br><br>
            Van harte welkom aan boord!
            <br>Tony van de Berg
